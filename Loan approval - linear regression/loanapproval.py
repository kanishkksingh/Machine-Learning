import numpy as np
import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import MinMaxScaler, OneHotEncoder
from sklearn.compose import ColumnTransformer
from sklearn.pipeline import Pipeline

# Separate features (X) and target (y)
# Create a copy to avoid modifying the original DataFrame directly
df_processed = df.copy()
X = df_processed.drop('loan_status', axis=1)
y = df_processed['loan_status']

# Identify numerical and categorical columns
numerical_features = X.select_dtypes(include=np.number).columns
categorical_features = X.select_dtypes(include='object').columns

# Create a preprocessing pipeline for numerical features (MinMaxScaler) and categorical features (OneHotEncoder)
preprocessor = ColumnTransformer(
    transformers=[
        ('num', MinMaxScaler(), numerical_features),
        ('cat', OneHotEncoder(handle_unknown='ignore'), categorical_features)
    ],
    remainder='passthrough' # Keep any other columns (e.g., if there were non-numeric, non-categorical)
)

# Apply the preprocessing steps to X
X_processed = preprocessor.fit_transform(X)

# If you want to convert X_processed back to a DataFrame with column names (optional, but good for inspection)
# Get feature names after one-hot encoding
encoded_feature_names = preprocessor.named_transformers_['cat'].get_feature_names_out(categorical_features)
all_feature_names = list(numerical_features) + list(encoded_feature_names)

X_final = pd.DataFrame(X_processed, columns=all_feature_names, index=X.index)

# Split the processed data into training and testing sets
X_train, X_test, y_train, y_test = train_test_split(X_final, y, test_size=0.2, random_state=42)

print("X_train shape after scaling and encoding:", X_train.shape)
print("X_test shape after scaling and encoding:", X_test.shape)
print("y_train shape:", y_train.shape)
print("y_test shape:", y_test.shape)

print("\nPreview of X_train (first 5 rows, first 10 columns):")
