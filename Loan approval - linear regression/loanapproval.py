"""#1. Via KNN MODEL-->"""

from sklearn.model_selection import train_test_split

X_train, X_test, y_train, y_test = train_test_split(X_final, y, test_size = 0.2, random_state = 42)

from sklearn.neighbors import KNeighborsClassifier

accuracy_list = []

from sklearn.metrics import accuracy_score
from sklearn.impute import SimpleImputer

# Clear the accuracy list before running the loop
accuracy_list = []

# Initialize imputer to fill NaNs, e.g., with the mean of the column
imputer = SimpleImputer(strategy='mean')

for i in range(1, 10):

  knn = KNeighborsClassifier(n_neighbors = i)

  # Impute NaNs in X_train and X_test before fitting
  # Fit the imputer on X_train and transform both training and test sets
  X_train_imputed = imputer.fit_transform(X_train)
  X_test_imputed = imputer.transform(X_test)

  knn.fit(X_train_imputed, y_train)

  # Finding the Predicted Values of the KNN Model

  y_prediction = knn.predict(X_test_imputed)

  # Finding the Accuracy

  acs = accuracy_score(y_test, y_prediction)

  # Adding the Accuracy Score to the List

  accuracy_list.append(acs)

plt.plot(range(1, len(accuracy_list) + 1), accuracy_list, marker = 'o')

plt.xlabel('Value of K')
plt.ylabel('Accuracy')
plt.title('Elbow Method for K-NN: Value of K vs. Accuracy')
plt.grid()

plt.show()

# Display the full accuracy list
print("Accuracy list for k=1 to 10:")
print(accuracy_list)

# Find the maximum accuracy and its corresponding k value
max_accuracy = max(accuracy_list)
best_k = accuracy_list.index(max_accuracy) + 1 # +1 because k starts from 1, not 0 index

print(f"\nThe maximum accuracy achieved is: {max_accuracy:.4f}")
print(f"The optimal 'k' value for this accuracy is: {best_k}")

